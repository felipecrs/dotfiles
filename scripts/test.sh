cat <<'EOM' | sudo tee /usr/local/bin/wslvar
#!/bin/bash

echo "Hello"
EOM

sudo chmod +x /usr/local/bin/wslvar

EOF
      )"

    elif [[ "${variant}" == "gnome" ]]; then
      run_test "${os}" "$(
        # shellcheck disable=SC2312
        cat <<'EOF'
sudo apt update --yes
sudo apt install -y --no-install-recommends gnome-shell
EOF
      )"
    fi
  done
done

