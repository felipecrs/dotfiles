  for os in "${oses[@]}"; do
    echo "Testing variant '${variant}' with OS '${os}'"

    if [[ "${variant}" == "devcontainer" ]]; then
      run_test "${os}" "export REMOTE_CONTAINERS=true"

    elif [[ "${variant}" == "wsl" ]]; then
      run_test "${os}" "$(
        # shellcheck disable=SC2312
        cat <<'EOF'
export IS_WSL=true

cat <<'EOM' | sudo tee /usr/local/bin/wslpath
